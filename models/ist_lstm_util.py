import pickle
import numpy as np
import os
import torch


class DataLoader(object):
    """数据加载器，适配pickle格式数据"""
    def __init__(self, xs, ys, batch_size, pad_with_last_sample=True):
        self.batch_size = batch_size
        self.current_ind = 0
        if pad_with_last_sample:
            num_padding = (batch_size - (len(xs) % batch_size)) % batch_size
            x_padding = np.repeat(xs[-1:], num_padding, axis=0)
            y_padding = np.repeat(ys[-1:], num_padding, axis=0)
            xs = np.concatenate([xs, x_padding], axis=0)
            ys = np.concatenate([ys, y_padding], axis=0)
        self.size = len(xs)
        self.num_batch = int(self.size // self.batch_size)
        self.xs = xs
        self.ys = ys

    def shuffle(self):
        permutation = np.random.permutation(self.size)
        self.xs, self.ys = self.xs[permutation], self.ys[permutation]

    def get_iterator(self):
        self.current_ind = 0

        def _wrapper():
            while self.current_ind < self.num_batch:
                start_ind = self.batch_size * self.current_ind
                end_ind = min(self.size, self.batch_size * (self.current_ind + 1))
                x_i = self.xs[start_ind: end_ind, ...]
                y_i = self.ys[start_ind: end_ind, ...]
                yield (x_i, y_i)
                self.current_ind += 1

        return _wrapper()


class StandardScaler():
    """标准化"""
    def __init__(self, mean, std):
        self.mean = mean
        self.std = std

    def transform(self, data):
        return (data - self.mean) / self.std

    def inverse_transform(self, data):
        return (data * self.std) + self.mean


def load_pickle_data(data_dir):
    """
    加载pickle格式的数据集
    返回: dict，包含train/val/test的x, y以及scaler
    """
    data = {}
    for category in ['train', 'val', 'test']:
        filepath = os.path.join(data_dir, category + '.pkl')
        with open(filepath, 'rb') as f:
            cat_data = pickle.load(f)
        data['x_' + category] = cat_data['x']
        data['y_' + category] = cat_data['y']
    
    # 只对第一个特征做标准化 (假设是流量)
    scaler = StandardScaler(
        mean=data['x_train'][..., 0].mean(), 
        std=data['x_train'][..., 0].std()
    )
    
    for category in ['train', 'val', 'test']:
        data['x_' + category][..., 0] = scaler.transform(data['x_' + category][..., 0])
        data['y_' + category][..., 0] = scaler.transform(data['y_' + category][..., 0])
    
    return data, scaler


def create_dataloaders(data, batch_size, valid_batch_size=None, test_batch_size=None):
    """创建数据加载器"""
    if valid_batch_size is None:
        valid_batch_size = batch_size
    if test_batch_size is None:
        test_batch_size = batch_size
    
    data['train_loader'] = DataLoader(data['x_train'], data['y_train'], batch_size)
    data['val_loader'] = DataLoader(data['x_val'], data['y_val'], valid_batch_size)
    data['test_loader'] = DataLoader(data['x_test'], data['y_test'], test_batch_size)
    
    return data


def load_adj_pickle(pkl_filepath):
    """加载pickle格式的邻接矩阵"""
    with open(pkl_filepath, 'rb') as f:
        adj = pickle.load(f)
    if isinstance(adj, np.ndarray):
        return adj.astype(np.float32)
    return adj


def masked_mse(preds, labels, null_val=np.nan):
    if np.isnan(null_val):
        mask = ~torch.isnan(labels)
    else:
        mask = (labels != null_val)
    mask = mask.float()
    mask /= torch.mean((mask))
    mask = torch.where(torch.isnan(mask), torch.zeros_like(mask), mask)
    loss = (preds - labels) ** 2
    loss = loss * mask
    loss = torch.where(torch.isnan(loss), torch.zeros_like(loss), loss)
    return torch.mean(loss)


